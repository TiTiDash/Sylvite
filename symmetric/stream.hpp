/**
 * @file stream.hpp
 * @brief Authenticated streaming encryption using XChaCha20-Poly1305.
 *
 * Provides `Encryptor` and `Decryptor` classes for streaming authenticated
 * encryption over `crypto_secretstream_xchacha20poly1305` (libsodium).
 *
 * Streaming encryption is ideal for large files or data that cannot fit
 * entirely in memory. The plaintext is split into chunks, each independently
 * authenticated with a Poly1305 MAC, and linked together so that reordering,
 * truncation, or chunk removal is detected.
 *
 * @par Stream file layout:
 * @code
 * [ header (24 bytes) | chunk_1 | chunk_2 | ... | chunk_N (TAG_FINAL) ]
 * @endcode
 * The header is generated by the `Encryptor` constructor and must be
 * transmitted to the `Decryptor` before any chunk is pulled.
 *
 * @par Chunk layout (each chunk):
 * @code
 * [ MAC (17 bytes, includes tag) | ciphertext (plaintext_size bytes) ]
 * @endcode
 * Overhead per chunk: `crypto_secretstream_xchacha20poly1305_ABYTES` (17 bytes).
 *
 * @par Typical usage:
 * @code
 *     // ── Encryption ────────────────────────────────────────────
 *     sylvite::symmetric::stream::XChaCha20Poly1305Stream::Encryptor enc(key);
 *     write_to_file(enc.header());                // transmit header first
 *     while (has_more_data()) {
 *         auto ct = enc.update(read_chunk());     // intermediate chunks
 *         write_to_file(ct);
 *     }
 *     write_to_file(enc.finalize(last_chunk));    // final chunk (TAG_FINAL)
 *
 *     // ── Decryption ────────────────────────────────────────────
 *     sylvite::symmetric::stream::XChaCha20Poly1305Stream::Decryptor dec(key, header);
 *     while (has_more_data()) {
 *         auto [plain, tag] = dec.pull(read_chunk());
 *         write_to_file(plain);
 *         if (tag == crypto_secretstream_xchacha20poly1305_TAG_FINAL) break;
 *     }
 * @endcode
 *
 * @par Security properties:
 *  - Confidentiality:   XChaCha20 (256-bit key, 192-bit nonce)
 *  - Authentication:    Poly1305 MAC per chunk
 *  - Stream integrity:  Chunk reordering, removal, and truncation are detected
 *  - Nonce management:  Handled internally by libsodium — never reused
 *
 * @par Thread safety:
 *  `Encryptor` and `Decryptor` are NOT thread-safe. Each instance must be
 *  used from a single thread. Create separate instances per thread.
 */

